"""
2D 动态规划 练习题（网格路径 + 背包）
"""
import sys


# ============================================================
# 最小路径和
# 模式：2D DP | I/O：单组测试用例
# ============================================================

def solve_min_path_sum():
    """
    输入格式：
        第一行 n m（网格行数和列数）
        接下来 n 行，每行 m 个整数
    输出格式：
        一行：从左上角到右下角的最小路径和（只能向右或向下）

    核心思路：
        dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + grid[i][j]
    """
    input = sys.stdin.readline

    n, m = map(int, input().split())
    grid = [list(map(int, input().split())) for _ in range(n)]

    # ============================================================
    # Algorithm：2D DP
    # ============================================================
    dp = [[0] * m for _ in range(n)]
    dp[0][0] = grid[0][0]

    for j in range(1, m):
        dp[0][j] = dp[0][j-1] + grid[0][j]

    for i in range(1, n):
        dp[i][0] = dp[i-1][0] + grid[i][0]

    for i in range(1, n):
        for j in range(1, m):
            dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + grid[i][j]

    # ============================================================
    # Output
    # ============================================================
    print(dp[n-1][m-1])


# ============================================================
# 0-1 背包
# 模式：2D DP（可滚动优化）| I/O：单组测试用例
# ============================================================

def solve_01_knapsack():
    """
    输入格式：
        第一行 n W（物品数量和背包容量）
        接下来 n 行，每行 w_i v_i（第 i 个物品的重量和价值）
    输出格式：
        一行：背包能装的最大价值

    核心思路：
        dp[i][w] = max(dp[i-1][w], dp[i-1][w-w_i] + v_i)
        滚动数组优化：j 从 W 逆序到 w_i（每个物品只用一次）
    """
    input = sys.stdin.readline

    n, W = map(int, input().split())
    items = []
    for _ in range(n):
        w, v = map(int, input().split())
        items.append((w, v))

    # ============================================================
    # Algorithm：滚动数组优化的 0-1 背包
    # ============================================================
    dp = [0] * (W + 1)

    for w, v in items:
        for j in range(W, w - 1, -1):  # [防错] 逆序遍历，每个物品只用一次
            dp[j] = max(dp[j], dp[j - w] + v)

    # ============================================================
    # Output
    # ============================================================
    print(dp[W])


# ============================================================
# 最长公共子序列 (LCS)
# 模式：2D DP | I/O：多组测试用例
# ============================================================

def solve_lcs():
    """
    输入格式：
        第一行 t（测试用例组数）
        每组两行字符串 s1, s2
    输出格式：
        每组一行：最长公共子序列的长度

    核心思路：
        dp[i][j] = s1[:i] 和 s2[:j] 的 LCS 长度
        if s1[i-1] == s2[j-1]: dp[i][j] = dp[i-1][j-1] + 1
        else: dp[i][j] = max(dp[i-1][j], dp[i][j-1])
    """
    input = sys.stdin.readline

    t = int(input())
    out = []

    for _ in range(t):
        s1 = input().strip()
        s2 = input().strip()
        n, m = len(s1), len(s2)

        # ============================================================
        # Algorithm：2D DP
        # ============================================================
        dp = [[0] * (m + 1) for _ in range(n + 1)]

        for i in range(1, n + 1):
            for j in range(1, m + 1):
                if s1[i - 1] == s2[j - 1]:
                    dp[i][j] = dp[i - 1][j - 1] + 1
                else:
                    dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])

        out.append(str(dp[n][m]))

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write("\n".join(out) + "\n")


if __name__ == "__main__":
    solve_lcs()
