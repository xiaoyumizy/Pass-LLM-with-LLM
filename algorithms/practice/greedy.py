import sys


# ============================================================
# 跳跃游戏（能到达最远位置）
# 模式：贪心 | I/O：单组测试用例
# ============================================================

def solve_jump_game():
    """
    输入格式：
        第一行 n
        第二行 n 个非负整数，表示每个位置能跳跃的最大长度
    输出格式：
        一行：能否到达最后一个位置（Yes/No）

    核心思路：
        维护当前能到达的最远位置 max_reach
        遍历每个位置，若当前位置 > max_reach 则不可达
    """
    input = sys.stdin.readline

    n = int(input())
    nums = list(map(int, input().split()))

    # ============================================================
    # Algorithm：贪心
    # ============================================================
    max_reach = 0
    for i in range(n):
        if i > max_reach:
            print("No")
            return
        max_reach = max(max_reach, i + nums[i])

    # ============================================================
    # Output
    # ============================================================
    print("Yes" if max_reach >= n - 1 else "No")


# ============================================================
# 区间调度（最多不重叠区间）
# 模式：贪心 + 排序 | I/O：单组测试用例
# ============================================================

def solve_interval_scheduling():
    """
    输入格式：
        第一行 n（区间数量）
        接下来 n 行，每行 start end（区间起止时间）
    输出格式：
        一行：最多能选择多少个不重叠的区间

    核心思路：
        按结束时间升序排序
        贪心选择最早结束的区间，然后选下一个开始 >= 当前结束的
    """
    input = sys.stdin.readline

    n = int(input())
    intervals = []
    for _ in range(n):
        s, e = map(int, input().split())
        intervals.append((s, e))

    # ============================================================
    # Algorithm：贪心（按结束时间排序）
    # ============================================================
    intervals.sort(key=lambda x: x[1])

    count = 0
    last_end = -1
    for s, e in intervals:
        if s >= last_end:
            count += 1
            last_end = e

    # ============================================================
    # Output
    # ============================================================
    print(count)


# ============================================================
# 分发糖果
# 模式：贪心 + 两次遍历 | I/O：单组测试用例
# ============================================================

def solve_candy():
    """
    输入格式：
        第一行 n
        第二行 n 个整数表示每个孩子的评分
    输出格式：
        一行：最少需要多少颗糖果（评分高的孩子必须比相邻的得到更多糖果）

    核心思路：
        两次遍历：
        左→右：若 rating[i] > rating[i-1]，candy[i] = candy[i-1] + 1
        右→左：若 rating[i] > rating[i+1]，candy[i] = max(candy[i], candy[i+1] + 1)
    """
    input = sys.stdin.readline

    n = int(input())
    ratings = list(map(int, input().split()))

    if n == 0:
        print(0)
        return

    # ============================================================
    # Algorithm：贪心 + 两次遍历
    # ============================================================
    candy = [1] * n

    # 左→右
    for i in range(1, n):
        if ratings[i] > ratings[i - 1]:
            candy[i] = candy[i - 1] + 1

    # 右→左
    for i in range(n - 2, -1, -1):
        if ratings[i] > ratings[i + 1]:
            candy[i] = max(candy[i], candy[i + 1] + 1)

    # ============================================================
    # Output
    # ============================================================
    print(sum(candy))


if __name__ == "__main__":
    solve_candy()
