"""
1D 动态规划 练习题
"""
import sys


# ============================================================
# 爬楼梯（组合计数 DP）
# 模式：1D DP | I/O：单组测试用例
# ============================================================

def solve_climbing_stairs():
    """
    输入格式：
        一行 n（台阶数）
    输出格式：
        一行：到达第 n 阶的方法数

    核心思路：
        dp[i] = dp[i-1] + dp[i-2]
        每次可以走 1 步或 2 步
    """
    input = sys.stdin.readline

    n = int(input())

    if n <= 2:
        print(n)
        return

    # ============================================================
    # Algorithm：1D DP
    # ============================================================
    prev2, prev1 = 1, 2
    for i in range(3, n + 1):
        curr = prev1 + prev2
        prev2 = prev1
        prev1 = curr

    # ============================================================
    # Output
    # ============================================================
    print(prev1)


# ============================================================
# 打家劫舍（不相邻最大和）
# 模式：1D DP | I/O：单组测试用例
# ============================================================

def solve_house_robber():
    """
    输入格式：
        第一行 n
        第二行 n 个正整数表示每家的金额
    输出格式：
        一行：能偷到的最大金额（不能偷相邻的两家）

    核心思路：
        dp[i] = max(dp[i-1], dp[i-2] + nums[i])
        不偷第 i 家 or 偷第 i 家
    """
    input = sys.stdin.readline

    n = int(input())
    nums = list(map(int, input().split()))

    if n == 0:
        print(0)
        return
    if n == 1:
        print(nums[0])
        return

    # ============================================================
    # Algorithm：1D DP（空间优化为两个变量）
    # ============================================================
    prev2, prev1 = 0, nums[0]
    for i in range(1, n):
        curr = max(prev1, prev2 + nums[i])
        prev2 = prev1
        prev1 = curr

    # ============================================================
    # Output
    # ============================================================
    print(prev1)


# ============================================================
# 最长递增子序列 (LIS)
# 模式：1D DP + 二分优化 | I/O：单组测试用例
# ============================================================

def solve_lis():
    """
    输入格式：
        第一行 n
        第二行 n 个整数
    输出格式：
        一行：最长严格递增子序列的长度

    核心思路：
        O(n²) DP：dp[i] = 以 a[i] 结尾的 LIS 长度
        dp[i] = max(dp[j] + 1) for j < i and a[j] < a[i]

        O(n log n) 贪心+二分：维护 tails 数组，tails[k] = 长度为 k+1 的 LIS 的最小末尾
    """
    input = sys.stdin.readline

    n = int(input())
    a = list(map(int, input().split()))

    # ============================================================
    # Algorithm：O(n log n) 二分优化
    # ============================================================
    import bisect

    tails = []  # tails[i] = 长度为 i+1 的递增子序列的最小末尾元素
    for x in a:
        pos = bisect.bisect_left(tails, x)
        if pos == len(tails):
            tails.append(x)
        else:
            tails[pos] = x

    # ============================================================
    # Output
    # ============================================================
    print(len(tails))


if __name__ == "__main__":
    solve_lis()
