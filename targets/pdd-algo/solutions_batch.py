import sys
import math


# ============================================================
# 第 1 题：发光二极管 LED 段数计数
# 模式：模拟 + 哈希映射 | I/O：逐行读到空行终止
# ============================================================

def solve_led():
    """
    输入格式：
        多行输入，每行一个数字字符串（不含前导零），空行终止输入
    输出格式：
        每行输出该数字的 LED 段数之和
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：预计算 0-9 的 LED 段数映射表（O(1) 查表）
    # ============================================================
    # 七段显示器各数字段数：0→6, 1→2, 2→5, 3→5, 4→4,
    #                        5→5, 6→6, 7→3, 8→7, 9→6
    led_map = {c: v for c, v in zip("0123456789", [6, 2, 5, 5, 4, 5, 6, 3, 7, 6])}

    # ============================================================
    # Algorithm：逐行读取，查表累加每个数字的段数
    # ============================================================
    out = []
    while True:
        line = input()
        if not line:                  # EOF 终止
            break
        line = line.strip()          # [防错] 必须 strip 去除末尾 \n，否则空字符干扰判断
        if not line:                  # 空行 = 输入终止标记
            break
        total = 0
        for c in line:
            total += led_map[c]       # O(1) 字典查表获取段数
        out.append(str(total))

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write("\n".join(out))


# ============================================================
# 第 2 题：扩散字符串变换（阴阳盘 + 状态机）
# 模式：模拟 + 状态机 | I/O：两行输入
# ============================================================

def solve_yin_yang():
    """
    输入格式：
        第一行：初始字符串 s
        第二行：操作序列 ops
    输出格式：
        输出最终字符串

    阴阳盘规则：
        阴盘(state=False)：翻转字符大小写
        阳盘(state=True)：字符保持不变
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取输入，字符串转 list 以便原地修改
    # ============================================================
    s = input().strip()              # [防错] 必须 strip 去除末尾 \n
    ops = input().strip()
    chars = list(s)                  # 字符串不可变，转 list 原地修改
    state = False                    # False=阴盘(翻转), True=阳盘(不变)

    # ============================================================
    # Algorithm：逐操作执行，每次后切换阴阳状态
    # ============================================================
    for i, op in enumerate(ops):
        pos = i % len(chars)         # 操作序号对长度取模 = 目标位置（0-based）
        c = chars[pos]
        if not state:                # 阴盘：翻转大小写
            if c.islower():
                chars[pos] = c.upper()
            else:
                chars[pos] = c.lower()
        # 阳盘：字符保持不变，无需操作

        state = not state            # 每次操作后切换阴阳状态

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write("".join(chars))


# ============================================================
# 第 3 题：循环指令糖果坐标覆盖计数
# 模式：数学 + 模分组 + 区间合并 | I/O：两行输入
# ============================================================

def solve_candy():
    """
    输入格式：
        第一行：三个整数 n m k（坐标范围、指令长度、循环次数）
        第二行：长度为 m 的操作字符串（R=右移, L=左移）
    输出格式：
        输出 k 轮指令后访问过的坐标总数

    核心思路：
        k 轮后坐标集合 = ⋃_{v∈V} {v + i·Δ | 0 ≤ i < k}
        按 x mod |Δ| 分组，同组等差数列不交叉，合并重叠区间后计数。
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取输入，跑一轮指令记录所有访问坐标和净位移
    # ============================================================
    n, m, k = map(int, input().split())
    ops = input().strip()

    pos = 0                          # 当前坐标（从 0 出发）
    visited = {0}                    # 一轮内访问过的坐标集合
    for op in ops:
        pos += 1 if op == 'R' else -1
        visited.add(pos)
    delta = pos                      # 一轮结束后的净位移（可为负）

    # ============================================================
    # Algorithm：按 Δ 分组，合并重叠区间计数
    # ============================================================
    # Δ = 0：每轮路径完全重合，答案 = 一轮的独立位置数
    if delta == 0:
        sys.stdout.write(str(len(visited)))
        return

    d = abs(delta)                   # 分组模数

    # 按坐标对 d 取模分组：同余坐标的等差数列互不交叉
    groups = {}
    for v in visited:
        r = v % d
        groups.setdefault(r, []).append(v)

    ans = 0
    # 对每组内的坐标（等差数列起始位置），合并重叠区间
    for offsets in groups.values():
        offsets.sort()
        lo = offsets[0]
        hi = offsets[0]
        for v in offsets[1:]:
            # 两段区间 [lo, lo+(k-1)d] 与 [v, v+(k-1)d] 重叠
            # 当且仅当 v <= hi + (k-1)*d
            if v <= hi + (k - 1) * d:
                hi = max(hi, v)       # 扩展右边界
            else:
                # 合并段贡献：公差 d 的等差数列并集，长度为 (hi-lo)/d + k
                ans += (hi - lo) // d + k
                lo = hi = v
        ans += (hi - lo) // d + k     # 最后一组

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write(str(ans))


