# 第 11 题：字符串按下标修改
# 模式：字符串按索引修改 | I/O：第一行字符串，第二行 m，后续 m 行操作
# ============================================================

def solve_str_modify():
    """
    输入格式：
        第一行：初始字符串 s
        第二行：整数 m（操作次数）
        后续 m 行：每行两个值（位置 字符），位置按 1 起始编号
    输出格式：
        输出修改后的最终字符串
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取输入，字符串转 list 以便原地修改
    # ============================================================
    s = list(input().strip())        # [防错] 必须 strip 去除末尾 \n
    m = int(input())

    # ============================================================
    # Algorithm：按操作逐条修改对应位置的字符
    # ============================================================
    for _ in range(m):
        parts = input().strip().split()  # [防错] 必须 strip 去除末尾 \n
        pos = int(parts[0]) - 1      # [防错] 题目 1 起始 → Python 0 起始，必须 -1
        s[pos] = parts[1]            # 直接赋值修改 list 元素

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write("".join(s))


