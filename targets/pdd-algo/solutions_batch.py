# 第 7 题：井字棋（博弈合法性模拟）
# 模式：模拟 + 状态检查 | I/O：单行输入
# ============================================================

def solve_tic_tac_toe():
    """
    输入格式：
        第一行：操作序列字符串（长度 = 总步数，每两个字符为一组坐标）
    输出格式：
        0 = 先手(X)赢, 1 = 后手(O)赢, 2 = 没人赢, -1 = 非法操作

    规则：每步前先检查上一步是否已赢（赢了还下 → -1）；
          落子位置有子 → -1；下完后检查当前玩家是否三连。
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取操作序列，初始化 3×3 棋盘
    # ============================================================
    ops = input().strip()
    board = [['.' for _ in range(3)] for _ in range(3)]
    players = ['X', 'O']             # 先手 X, 后手 O

    # ============================================================
    # Algorithm：模拟每一步落子，实时检查合法性
    # ============================================================
    def check_win(board, player):
        """检查 player 是否三连：3 行 + 3 列 + 2 对角线"""
        for r in range(3):
            if all(board[r][c] == player for c in range(3)):
                return True
        for c in range(3):
            if all(board[r][c] == player for r in range(3)):
                return True
        if all(board[i][i] == player for i in range(3)):
            return True
        if all(board[i][2 - i] == player for i in range(3)):
            return True
        return False

    for step in range(0, len(ops), 2):
        move_idx = step // 2
        player = players[move_idx % 2]
        # 操作字符串是连续数字，每两个字符为一组坐标（如 "13" = 第1行第3列）
        r = int(ops[step]) - 1        # [防错] 题目 1 起始 → Python 0 起始，必须 -1
        c = int(ops[step + 1]) - 1

        if step > 0 and check_win(board, players[(move_idx - 1) % 2]):
            sys.stdout.write("-1")    # 上一步已赢但继续操作 → 非法
            return
        if board[r][c] != '.':
            sys.stdout.write("-1")    # 落子位置已有子 → 非法
            return

        board[r][c] = player

    # ============================================================
    # Output：终局判定
    # ============================================================
    if check_win(board, 'X'):
        sys.stdout.write("0")
    elif check_win(board, 'O'):
        sys.stdout.write("1")
    else:
        sys.stdout.write("2")


