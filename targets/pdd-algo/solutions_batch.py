# 第 9 题：质因子分解计数（数论）
# 模式：数学推导 + 公式 | I/O：单行输入
# ============================================================

def solve_factor_count():
    """
    输入格式：
        一行：三个整数 a m x
    输出格式：
        输出满足 a * b^x 整除 x^b 的 b 的个数（1 <= b <= m）

    核心：分解 a 和 x 的质因子，逐因子检查约束。
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取输入
    # ============================================================
    a, m, x = map(int, input().split())

    # ============================================================
    # Algorithm：试除法质因数分解，逐因子求 b 的下界
    # ============================================================
    def factorize(n):
        """试除法质因数分解，返回 {质因子: 次数} 字典。偶数后只试奇数。"""
        factors = {}
        d = 2
        while d * d <= n:
            while n % d == 0:
                factors[d] = factors.get(d, 0) + 1
                n //= d
            d += 1 if d == 2 else 2  # [防错] 2 之后只试奇数，减半迭代次数
        if n > 1:                     # 剩余的 n 本身是质数
            factors[n] = factors.get(n, 0) + 1
        return factors

    fx = factorize(x)
    fa = factorize(a)

    # a 含 x 没有的质因子 → 无解（a*b^x 不可能整除 x^b）
    for p in fa:
        if p not in fx:
            sys.stdout.write("0")
            return

    # 对 x 的每个质因子 p，计算 b 的下界
    min_b = 1
    for p in fx:
        need = fa.get(p, 0)           # a 中 p 的次数
        have = fx[p]                  # x 中 p 的次数（x^b 中为 b*have）
        # b*have >= need  →  b >= ceil(need / have)
        b_min = (need + have - 1) // have
        min_b = max(min_b, b_min)

    # ============================================================
    # Output
    # ============================================================
    ans = max(0, m - min_b + 1)
    sys.stdout.write(str(ans))


