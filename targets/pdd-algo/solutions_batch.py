# 第 5 题：栈中熔合（GCD 合并）
# 模式：栈 + GCD 合并 | I/O：两行输入
# ============================================================

def solve_stack_merge():
    """
    输入格式：
        第一行：n S（序列长度、GCD 阈值）
        第二行：n 个整数
    输出格式：
        输出最终栈的元素个数

    规则：栈顶两元素的 gcd < S → 弹掉两元素，压入 gcd。
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取输入
    # ============================================================
    n, S = map(int, input().split())
    a = list(map(int, input().split()))

    # ============================================================
    # Algorithm：逐个入栈，反复检查栈顶两元素的 gcd
    # ============================================================
    stack = []
    for x in a:
        stack.append(x)
        # gcd 值 ≤ 原值，不需要回退指针，while 循环自动续检
        while len(stack) >= 2:
            g = math.gcd(stack[-1], stack[-2])
            if g < S:
                stack.pop()           # [防错] 先弹栈顶
                stack.pop()           # 再弹次栈顶（此时原栈顶已移除）
                stack.append(g)       # 压入 gcd 结果
            else:
                break                 # gcd >= S，停止合并

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write(str(len(stack)))


