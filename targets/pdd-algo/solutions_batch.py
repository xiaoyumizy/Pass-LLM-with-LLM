# 第 10 题：括号匹配合法性检查
# 模式：栈 + 括号匹配 | I/O：单行输入
# ============================================================

def solve_brackets():
    """
    输入格式：
        一行：括号字符串
    输出格式：
        "valid" = 合法括号序列, "invalid" = 非法
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取字符串
    # ============================================================
    s = input().strip()

    # ============================================================
    # Algorithm：栈 + 右→左括号映射，检查闭合和空栈
    # ============================================================
    stack = []
    pairs = {')': '(', ']': '[', '}': '{'}

    for c in s:
        if c in '([{':
            stack.append(c)           # 左括号入栈
        elif c in ')]}':
            if not stack or stack[-1] != pairs[c]:
                sys.stdout.write("invalid")  # [防错] 必须同时检查栈空和栈顶匹配
                return
            stack.pop()               # 匹配成功，弹出左括号

    # ============================================================
    # Output
    # ============================================================
    # 遍历完毕栈空 = 所有括号正确闭合
    # [防错] 必须检查 both 合法匹配 AND 栈空（有未闭合的左括号也非法）
    sys.stdout.write("valid" if not stack else "invalid")


