# 第 6 题：排列 mex 计数（最小覆盖窗口）
# 模式：数学观察 + 增量区间追踪 | I/O：两行输入
# ============================================================

def solve_mex_count():
    """
    输入格式：
        第一行：整数 n
        第二行：n 个不同整数，构成 0~n-1 的排列
    输出格式：
        输出 (n-1) - 去重后的 {L_2, L_3, ..., L_n} 的个数

    核心：
        mex >= v 当且仅当窗口包含 [0, v-1]
        L_v = 覆盖 [0, v-1] 的最短窗口长度
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取排列，构建值→位置的反向映射
    # ============================================================
    n = int(input())
    p = list(map(int, input().split()))

    # pos[x] = 值为 x 的元素在排列中的下标（0-based）
    # [双重角色] 既是位置查找表，又是增量追踪 [0,v-1] 覆盖范围的数据源
    pos = [0] * n
    for i, val in enumerate(p):
        pos[val] = i

    # ============================================================
    # Algorithm：v 递增时增量更新 [0, v-1] 的最左和最右位置
    # ============================================================
    unique_L = set()
    min_pos = n      # 当前 [0, v-1] 的最小下标（初始化为最大不可能值）
    max_pos = -1     # 当前 [0, v-1] 的最大下标

    for v in range(2, n + 1):
        pv = pos[v - 1]              # 新加入值 v-1 的位置
        if pv < min_pos:
            min_pos = pv             # 更新最左边界
        if pv > max_pos:
            max_pos = pv             # 更新最右边界
        L = max_pos - min_pos + 1    # 覆盖 [0, v-1] 的最短窗口长度
        unique_L.add(L)

    ans = (n - 1) - len(unique_L)

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write(str(ans))


