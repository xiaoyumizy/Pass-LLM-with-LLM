# 第 4 题：榨汁机（贪吃鬼模拟）
# 模式：模拟 + 计数器 | I/O：两行输入
# ============================================================

def solve_juicer():
    """
    输入格式：
        第一行：n S（水果数、容量上限）
        第二行：n 个整数 a_i（每个水果的体积）
    输出格式：
        输出榨汁启动次数

    贪吃鬼逻辑：机内水果总超过 S → 交给贪吃鬼，榨汁启动，机内清空。
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取输入
    # ============================================================
    n, S = map(int, input().split())
    a = list(map(int, input().split()))

    # ============================================================
    # Algorithm：贪心模拟，机内水果累加到溢出则启动榨汁
    # ============================================================
    cur = 0       # 机内当前水果体积
    count = 0     # 榨汁启动次数
    for v in a:
        if cur + v > S:             # 放入会溢出，先启动清空
            count += 1
            cur = v                  # 当前水果放入新一批
        else:
            cur += v                 # 放入当前批次

    # ============================================================
    # Output
    # ============================================================
    # 遍历完毕后机内剩余水果仍需启动一次
    if cur > 0:                     # [防错] 不能遗漏最后一轮
        count += 1
    sys.stdout.write(str(count))


