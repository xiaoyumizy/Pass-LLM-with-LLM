# 第 8 题：能力观赏排序（贪心 + 前缀和）
# 模式：贪心 + 前缀和 | I/O：两行输入
# ============================================================

def solve_showcase():
    """
    输入格式：
        第一行：n（参与人数）
        第二行：n 个整数 a_i（每个人的能力值）
    输出格式：
        输出最大观赏值总和

    策略：每轮人数减半向上取整，取剩余人中最强的。
    """
    input = sys.stdin.readline

    # ============================================================
    # Preprocess：读取输入，降序排列，预计算前缀和
    # ============================================================
    n = int(input())
    a = list(map(int, input().split()))
    a.sort(reverse=True)             # 降序：每轮取最强的人观赏值最大
    pre = [0]
    for x in a:
        pre.append(pre[-1] + x)
    # [双重角色] pre 既存储累加值，又支持 O(1) 区间求和查询

    # ============================================================
    # Algorithm：逐轮取最强的人，人数减半向上取整
    # ============================================================
    ans = 0
    idx = 0      # 已观赏过的人的起始下标
    cur_n = n    # 当前轮次剩余人数
    while cur_n > 0:
        # 本轮观赏的人 = a[idx] 到 a[idx+cur_n-1]，前缀和 O(1) 求区间和
        ans += pre[idx + cur_n] - pre[idx]
        idx += cur_n
        if idx >= n:                 # 所有人已观赏完毕
            break
        # 人数减半向上取整：ceil(cur_n / 2) = (cur_n + 1) // 2
        cur_n = (cur_n + 1) // 2

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write(str(ans))


