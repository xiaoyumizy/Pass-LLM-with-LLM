"""
差分数组 练习题
"""
import sys


# ============================================================
# 区间加操作 + 还原
# 模式：差分数组 | I/O：单组测试用例
# ============================================================

def solve_range_add():
    """
    输入格式：
        第一行 n q（数组长度和操作次数）
        第二行 n 个整数（初始数组）
        接下来 q 行，每行 l r val（对 [l, r] 区间每个元素加 val，1-based）
    输出格式：
        一行 n 个整数（操作后的数组）

    核心思路：
        构建差分数组 diff
        每次操作：diff[l] += val, diff[r+1] -= val
        还原：前缀和累加 diff，加到原数组上
    """
    input = sys.stdin.readline

    n, q = map(int, input().split())
    a = list(map(int, input().split()))

    # ============================================================
    # Preprocess：构建差分数组
    # ============================================================
    diff = [0] * (n + 1)

    for _ in range(q):
        l, r, val = map(int, input().split())
        l -= 1; r -= 1              # [防错] 转为 0-based
        diff[l] += val
        diff[r + 1] -= val          # [防错] diff 长度为 n+1，r+1 不会越界

    # ============================================================
    # Algorithm：前缀和还原差分数组
    # ============================================================
    result = [0] * n
    running = 0
    for i in range(n):
        running += diff[i]
        result[i] = a[i] + running

    # ============================================================
    # Output
    # ============================================================
    print(" ".join(map(str, result)))


# ============================================================
# 区间覆盖计数（差分思想）
# 模式：差分 + 前缀和 | I/O：单组测试用例
# ============================================================

def solve_interval_cover():
    """
    输入格式：
        第一行 n（数轴长度，下标 1~n）
        第二行 m（区间数量）
        接下来 m 行，每行 l r（区间 [l, r]，1-based）
    输出格式：
        一行 n 个整数：每个位置被覆盖的次数

    核心思路：
        差分数组统计每个位置被覆盖的次数
        diff[l] += 1, diff[r+1] -= 1
        还原后每个位置的值就是覆盖次数
    """
    input = sys.stdin.readline

    n = int(input())
    m = int(input())

    # ============================================================
    # Preprocess：构建差分数组
    # ============================================================
    diff = [0] * (n + 2)

    for _ in range(m):
        l, r = map(int, input().split())
        diff[l] += 1
        diff[r + 1] -= 1

    # ============================================================
    # Algorithm：前缀和还原
    # ============================================================
    result = [0] * n
    running = 0
    for i in range(1, n + 1):
        running += diff[i]
        result[i - 1] = running

    # ============================================================
    # Output
    # ============================================================
    print(" ".join(map(str, result)))


if __name__ == "__main__":
    solve_interval_cover()
