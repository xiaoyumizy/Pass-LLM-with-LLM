"""
位运算 练习题
"""
import sys


# ============================================================
# 只出现一次的数字（XOR）
# 模式：位运算 | I/O：单组测试用例
# ============================================================

def solve_single_number():
    """
    输入格式：
        第一行 n
        第二行 n 个整数（每个元素出现两次，只有一个出现一次）
    输出格式：
        一行：那个只出现一次的数

    核心思路：
        XOR 全部元素：a ^ a = 0，a ^ 0 = a
        所有出现两次的数互相抵消，剩下的就是答案
    """
    input = sys.stdin.readline

    n = int(input())
    nums = list(map(int, input().split()))

    # ============================================================
    # Algorithm：XOR 全部
    # ============================================================
    result = 0
    for x in nums:
        result ^= x

    # ============================================================
    # Output
    # ============================================================
    print(result)


# ============================================================
# 汉明距离（两个整数二进制不同位数）
# 模式：位运算 | I/O：多组测试用例
# ============================================================

def solve_hamming_distance():
    """
    输入格式：
        第一行 t（测试用例组数）
        每组一行两个整数 a b
    输出格式：
        每组一行：a 和 b 的汉明距离

    核心思路：
        XOR 后统计 1 的个数
        x & (x-1) 去掉最低位的 1，循环计数
    """
    input = sys.stdin.readline

    t = int(input())
    out = []

    for _ in range(t):
        a, b = map(int, input().split())

        # ============================================================
        # Algorithm：XOR + 计数 1 的个数
        # ============================================================
        xor = a ^ b
        count = 0
        while xor:
            xor &= xor - 1    # [防错] 去掉最低位的 1，比逐位检查更快
            count += 1

        out.append(str(count))

    # ============================================================
    # Output
    # ============================================================
    sys.stdout.write("\n".join(out) + "\n")


# ============================================================
# 子集枚举（bitmask）
# 模式：位运算 | I/O：单组测试用例
# ============================================================

def solve_subset_sum():
    """
    输入格式：
        第一行 n target
        第二行 n 个整数
    输出格式：
        一行：是否存在子集使得元素之和等于 target（Yes/No）

    核心思路：
        枚举所有子集 mask ∈ [0, 2^n)
        mask & (1 << i) 检查第 i 个元素是否在子集中
    """
    input = sys.stdin.readline

    n, target = map(int, input().split())
    nums = list(map(int, input().split()))

    # ============================================================
    # Algorithm：bitmask 子集枚举
    # ============================================================
    found = False
    for mask in range(1 << n):
        subset_sum = 0
        for i in range(n):
            if mask & (1 << i):
                subset_sum += nums[i]
        if subset_sum == target:
            found = True
            break

    # ============================================================
    # Output
    # ============================================================
    print("Yes" if found else "No")


if __name__ == "__main__":
    solve_subset_sum()
